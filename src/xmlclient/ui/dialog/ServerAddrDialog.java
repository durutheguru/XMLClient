    private void localhostCBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_localhostCBoxActionPerformed
        // TODO add your handling code here:
        if (localhostCBox.isSelected()){
            addrField.setText("127.0.0.1");        
            addrField.setEnabled(false);
            portField.setText("12345");
        }
        else{
            addrField.setText("");
            addrField.setEnabled(true);
            portField.setText("");
        }
    }//GEN-LAST:event_localhostCBoxActionPerformed

    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        if (addrField.getText().equals("") || portField.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please supply the required field", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
            Integer.parseInt(portField.getText());
        }
        catch(NumberFormatException ex){            
            JOptionPane.showMessageDialog(this, "Please supply a valid port number", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        serverIP = addrField.getText();
        port = portField.getText();
        filledData = true;
        dispose();
    }//GEN-LAST:event_okButtonActionPerformed

    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        // TODO add your handling code here:
        filledData = false;
        dispose();
    }//GEN-LAST:event_cancelButtonActionPerformed

