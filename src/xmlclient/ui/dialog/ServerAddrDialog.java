    public static String[] getServerDetails(JFrame parent){
        ServerAddrDialog saDiag = new ServerAddrDialog(parent, true);
        saDiag.setVisible(true);
        if (saDiag.filledData){
            return new String[]{saDiag.serverIP, saDiag.port};
        }
        else
            return null;
    }
        
